    public void iniciar(int index){
        ResultSet resultados = base.consultar("SELECT a.IDCLIENTES,a.NOMBRE, a.APELLIDOS,\n" +
"b.DESTINO,\n" +
"b.FECHALLEGADA,\n" +
"b.FECHASALIDA,\n" +
"b.AUTOBUS,\n" +
"b.NOMBREHOTEL,\n" +
"b.NINOS,\n" +
"b.ADULTOS,\n" +
"c.NUMHAB,\n" +
"c.TIPOHAB,\n" +
"c.SERVICIOHOTEL \n" +
"FROM APP.CLIENTES a, APP.RESERVAS b , APP.HOTELRESERVAS c\n" +
"WHERE (a.IDCLIENTES="+index+" and b.IDCLIENTES="+index+" and c.IDCLIENTES="+index+")");
        if (resultados != null) {
            try {
                while (resultados.next()) {
                    jAdultos.setText(resultados.getString("ADULTOS"));
                    jApellidos.setText(resultados.getString("APELLIDOS"));
                    jAutobus.setText(resultados.getString("AUTOBUS"));
                    jDestino.setText(resultados.getString("DESTINO"));
                    jHotel.setText(resultados.getString("NOMBREHOTEL"));
                    jIDcliente.setText(resultados.getString("IDCLIENTES"));
                    jLlegada.setText(resultados.getString("FECHALLEGADA"));
                    jNinios.setText(resultados.getString("NINOS"));
                    jNombre.setText(resultados.getString("NOMBRE"));
                    jSalida.setText(resultados.getString("FECHASALIDA"));
                    jServicios.setText(resultados.getString("SERVICIOHOTEL"));
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        Random rnd = new Random();
        
        jTotal.setText("$"+String.valueOf(rnd.nextInt(1000)+3000));
    }
        
